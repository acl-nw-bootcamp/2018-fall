var name = "Mark Fitzgerald";
var greeting = "Hello! My name is " + name + ".";
console.log(greeting);

var student = "John Doe";
var response = `Hello. My name is ${student}. I'm happy to be here.`;
console.log(response);

console.log(`
  String Interpolation:
  Greeting: ${greeting}
  Response: ${response}
  `);
